#include "libft.h"

void	ft_putllnbr(long long n)
{
	long long i;

	i = 0;
	if (n >= 0)
		i = n;
	if (n < 0)
	{
		i = n * -1;
		ft_putchar('-');
	}
	if (i >= 10)
		ft_putllnbr(i / 10);
	ft_putchar((i % 10) + '0');
}
