int	ft_count_if(char **tab, int (*f)(char*))
{
	int count;
	int a;

	count = 0;
	a = 0;
	while (tab[a] != '\0')
	{
		if (f(tab[a]) == 1)
			count++;
		a++;
	}
	return (count);
}
